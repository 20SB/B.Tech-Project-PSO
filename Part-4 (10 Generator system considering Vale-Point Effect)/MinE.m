function [Temission]=MinE(Pg)
% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% %%This is the objective function for minimum emission for 10 generator system%%%
% %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
 
ng=10;
totPd=2000;%pu
%0.01*(alpha+beta*Pgi+gamma*Pgi2)+zeta*exp(lambda*Pgi)
d=[360.0012 350.0056 330.0056 330.0056 13.8593 13.8593 40.2669 40.2669 42.8955 42.8955];%alpha
e=[-3.9864 -3.9824 -3.9023 -3.9023 0.3277 0.3277 -0.5455 -0.5455 -0.5112 -0.5112];%beta
f=[0.04702 0.04652 0.04652 0.04652 0.00420 0.00420 0.00680 0.00680 0.00460 0.00460];%gamma
g=[0.25475 0.25475 0.25163 0.25163 0.24970 0.24970 0.24800 0.24990 0.25470 0.25470];%zeta
h=[0.01234 0.01234 0.01215 0.01215 0.01200 0.01200 0.01290 0.01203 0.01234 0.01234];%phi

E=zeros(1,ng); 
Temission=0;
for i=1:ng
    E(i)=(d(i)+e(i)*Pg(i)+f(i)*(Pg(i)^2))+g(i)*exp(h(i)*Pg(i));
    Temission=Temission+E(i);
end